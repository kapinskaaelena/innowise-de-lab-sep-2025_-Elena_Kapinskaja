--  Шаг 1: Вставка новых сотрудников
-- Почему это важно: INSERT — это как знакомство. Мы добавляем новых людей в нашу таблицу.
-- SERIAL означает, что EmployeeID создаётся автоматически — вручную его писать не нужно.
-- Добавляем двух сотрудников: Frank Miller (финансы) и Grace Lee (IT)
-- Обрати внимание: зарплата указывается как DECIMAL, с двумя знаками после запятой
INSERT INTO Employees (FirstName, LastName, Department, Salary)
VALUES 
    ('Frank', 'Miller', 'Finance', 67000.00),  -- Финансовый аналитик, средняя зарплата
    ('Grace', 'Lee', 'IT', 72000.00);          -- Разработчица, чуть выше среднего

--  Шаг 2: Выбор всех сотрудников
-- SELECT * — это как сказать: «Покажи мне всё!»
-- Мы получим все столбцы и все строки, чтобы убедиться, что новые сотрудники добавлены
-- Это полезно после любой операции INSERT, UPDATE или DELETE — как финальная проверка
SELECT * FROM Employees;

--  Шаг 3: Выбор сотрудников из отдела IT
-- Здесь мы фильтруем по Department = 'IT'
-- SELECT FirstName, LastName — значит, нам интересны только имена и фамилии
-- Это удобно, когда нужно составить список, например, для рассылки или отчёта
-- Важно: SQL чувствителен к точному написанию строки — 'IT' должно быть в кавычках и без пробелов
SELECT FirstName, LastName 
FROM Employees 
WHERE Department = 'IT';

--  Шаг 4: Обновление зарплаты Alice Smith
-- UPDATE — это как сказать: «У нас для тебя хорошие новости!»
-- Мы меняем только Salary, остальные поля остаются как есть
-- WHERE — это фильтр, чтобы не обновить всех подряд
-- Важно: если бы было несколько Alice Smith, нужно было бы уточнить, например, по EmployeeID
UPDATE Employees
SET Salary = 65000.00  -- Новая зарплата, возможно после повышения или пересмотра
WHERE FirstName = 'Alice' AND LastName = 'Smith';  -- Фильтр по имени и фамилии

--  Шаг 5: Удаление сотрудника Diana Prince
-- DELETE — это как увольнение, но без драмы
-- Мы удаляем строку, где фамилия Prince
-- Важно: если бы было несколько сотрудников с такой фамилией, удалились бы все — будь осторожна!
-- Лучше всегда уточнять фильтр, особенно в реальных базах
DELETE FROM Employees
WHERE LastName = 'Prince';  -- Удаляем Diana Prince — возможно, она ушла в другой отдел

-- Шаг 6: Финальная проверка
-- SELECT * снова покажет нам актуальное состояние таблицы
-- Это как сказать: «Давайте посмотрим, что получилось после всех изменений»
-- Очень полезно для отладки и визуального контроля
SELECT * FROM Employees; 